package cf.dlwlrma5.javatutorials.numberstring;

public class CharString {

	public static void main(String[] args) {
		
		System.out.println("Y");		//문자''
		System.out.println("Yoongit");		//문자열""
		System.out.println("Yoon"+"git");		//문자열도 연산가능
		
		
		System.out.println("Yoon said, \"hi, Yoongit\"");		// \를 "앞에 위치시키면 단순히 문자로 해석함
		
		System.out.println("Yoon said,\n \"hi, Yoongit\"");		// \n은 줄바꿈
	}

}
